class Main {
    // Function to multiply two numbers
    public static int multiply(int x, int y) {
        return x * y;
    }

    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        int product = multiply(a, b); // call function
        System.out.println("The product of a and b is: " + product);
    }
}
